#include"Coup.h"

Coup::Coup()
{
	 x=0 ;
	 y=0;
}

////////////////////////////////////////////////////////////////////////

Coup::Coup(int a, int b)
{
		 x=a;
		 y=b;
}

////////////////////////////////////////////////////////////////////////

int Coup::Getx()
{
	return(x);
}

////////////////////////////////////////////////////////////////////////

int Coup::Gety()
{
	return(y);
}

////////////////////////////////////////////////////////////////////////

bool operator==(Coup  a, Coup  b)
{

    if (a.Getx() == b.Getx() && a.Gety() == b.Gety())return true;
    else return false;

}
