#include "Case.h"
#include<iostream>


Case::Case()
{
	x=0;
	y=0;
	color="White";
}
////////////////////////////////////////////////////////////////////////
Case::Case(int a, int b, string clr)
{
	x=a;
	y=b;
	color=clr;
}
////////////////////////////////////////////////////////////////////////

int Case::getx()
{
	return x;
}

////////////////////////////////////////////////////////////////////////

int Case::gety()
{
	return y;
}

////////////////////////////////////////////////////////////////////////

string Case::getc()
{
	return color;
}

////////////////////////////////////////////////////////////////////////

void Case::setColorCase(string s)
{	
	color=s;
}

////////////////////////////////////////////////////////////////////////

bool Case::getVide()
{
	if (color== "White")return true;
		return false;
}

